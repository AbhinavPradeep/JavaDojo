class Relational_Operators
{
    public static void main(String[] args)
    {
        int num1;
        int num2;
        
        num1 = 213;
        num2 = 343;
        
        System.out.println(num1 > num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 < num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
    }
}
